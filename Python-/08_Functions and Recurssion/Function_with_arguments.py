def goodDay(name, ending):
    print("Good Day, " + name)
    print(ending)
    return "ok"

a = goodDay("Harry", "Thank you") 
print(a)

def badDay(name, end):
    print("Bad Day, " + name)
    print(end)
b = badDay("Sandipan", "OK")
print(b)

